"""
Experiment: Bioelectric Counter-Curvature Coupling

This script tests the hypothesis that macroscopic piezoelectric fields
generated by spinal bending act as an "Information Bus" for corrective
counter-bending (galvanotaxis/electrotaxis-driven growth).

Hypothesis (H_2026_03_14_Bioelectric_Gain):
Loss of the bioelectric field (e.g., via hydration-induced short-circuiting)
triggers scoliotic buckling by blinding the spine to instantaneous global strain.
"""

import argparse
import csv
import os
import sys
from datetime import datetime
from pathlib import Path

import numpy as np

# Ensure src is in python path
sys.path.append(str(Path(__file__).parent.parent.parent / "src"))

from spinalmodes.iec import IECParameters, solve_beam_static


def run_bioelectric_experiment(
    out_file: str,
    gamma_pe_values: list[float],
    gamma_et_values: list[float],
    load_p: float = 50.0,
):
    """
    Run parameter sweep simulating bioelectric corrective feedback.

    Args:
        out_file: Path to save CSV results.
        gamma_pe_values: List of Piezoelectric Voltage Coefficients.
        gamma_et_values: List of Electrotactic Growth Gains.
        load_p: Tip load acting as a buckling perturbation.
    """
    print("=" * 80)
    print("EXPERIMENT: Bioelectric Counter-Curvature")
    print(f"Output: {out_file}")
    print("=" * 80)

    os.makedirs(os.path.dirname(out_file), exist_ok=True)

    fieldnames = [
        "timestamp",
        "gamma_pe",
        "gamma_et",
        "bioelectric_gain",
        "load_p",
        "max_deflection",
        "max_curvature",
        "max_voltage_gradient",
        "bioelectric_moment_max",
    ]

    # Baseline physical properties for a 1D column (simplified spine)
    length = 0.5
    n_nodes = 100
    params = IECParameters(length=length, n_nodes=n_nodes, E0=1e6)
    s = params.s_array

    I_moment = 1e-8
    E_field = np.full_like(s, params.E0)
    kappa_target = np.zeros_like(s)

    with open(out_file, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()

        for gamma_pe in gamma_pe_values:
            for gamma_et in gamma_et_values:
                # Global Bioelectric Gain
                g_bio = gamma_pe * gamma_et

                # 1. Base solve: What is the strain/curvature under load without feedback?
                # We use the static solver iteratively to approximate feedback
                # Initial guess: No active moment
                theta, kappa = solve_beam_static(
                    s=s,
                    kappa_target=kappa_target,
                    E_field=E_field,
                    M_active=np.zeros_like(s),
                    I_moment=I_moment,
                    P_load=load_p,
                )

                # Iterative convergence for bioelectric feedback
                # M_bio(s) = Gamma_ET * E_bio(s) = Gamma_ET * Gamma_PE * kappa(s)
                # (Approximating strain gradient ~ curvature)
                max_iters = 100
                tolerance = 1e-6
                converged = False

                # Dynamic under-relaxation factor based on gain
                # High gain needs much stronger under-relaxation to prevent oscillation
                alpha = min(0.1, 0.5 / max(1.0, g_bio))

                M_active = np.zeros_like(s)

                for i in range(max_iters):
                    prev_kappa = kappa.copy()

                    # Calculate Voltage Field: E_bio = Gamma_PE * kappa
                    E_bio = gamma_pe * kappa

                    # Calculate Active Counter-Moment (opposite to curvature)
                    # We bound the active moment to physically reasonable limits to avoid overflow
                    new_M_active = np.clip(-gamma_et * E_bio, -1000, 1000)

                    # Apply under-relaxation to stabilize the iteration
                    M_active = alpha * new_M_active + (1.0 - alpha) * M_active

                    # Re-solve with new active moment
                    theta, kappa = solve_beam_static(
                        s=s,
                        kappa_target=kappa_target,
                        E_field=E_field,
                        M_active=M_active,
                        I_moment=I_moment,
                        P_load=load_p,
                    )

                    diff = np.max(np.abs(kappa - prev_kappa))
                    if diff < tolerance:
                        converged = True
                        break

                max_defl = np.max(np.abs(theta)) * length  # Approx tip deflection
                max_curv = np.max(np.abs(kappa))
                max_voltage = np.max(np.abs(gamma_pe * kappa))
                max_moment = np.max(np.abs(M_active))

                row = {
                    "timestamp": datetime.now().isoformat(),
                    "gamma_pe": gamma_pe,
                    "gamma_et": gamma_et,
                    "bioelectric_gain": g_bio,
                    "load_p": load_p,
                    "max_deflection": round(max_defl, 6),
                    "max_curvature": round(max_curv, 6),
                    "max_voltage_gradient": round(max_voltage, 6),
                    "bioelectric_moment_max": round(max_moment, 6),
                }

                writer.writerow(row)
                f.flush()

                # Print progress
                status = "Converged" if converged else "MaxIters"
                print(
                    f"PE={gamma_pe:<6.1f} | ET={gamma_et:<6.1f} | "
                    f"Gain={g_bio:<8.1f} | Defl={max_defl:<8.4f} | "
                    f"Curv={max_curv:<8.4f} ({status})"
                )

    print("Experiment Complete.")


def parse_args():
    parser = argparse.ArgumentParser(description="Bioelectric Coupling Experiment")
    parser.add_argument(
        "--out-file",
        default="outputs/bioelectric/bioelectric_sweep.csv",
        help="Path to save output CSV.",
    )
    return parser.parse_args()


if __name__ == "__main__":
    args = parse_args()

    # Define biologically inspired ranges
    # Gamma_PE: Voltage generated per unit curvature
    # High: Healthy dry bone (high piezoelectricity)
    # Low: Hyper-hydrated/swollen tissue (short-circuited)
    gamma_pe_vals = [0.0, 1.0, 5.0, 10.0, 20.0]

    # Gamma_ET: Corrective moment generated per unit voltage
    # High: Healthy cellular mechanotransduction
    # Low: Metabolic deficit / electrotactic deafness
    gamma_et_vals = [0.0, 0.1, 0.5, 1.0, 5.0]

    run_bioelectric_experiment(
        out_file=args.out_file,
        gamma_pe_values=gamma_pe_vals,
        gamma_et_values=gamma_et_vals,
        load_p=50.0,
    )
